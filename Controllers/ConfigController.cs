        public MessageResults Add(ConfigSystem config)
        {
            MessageResults _result = new MessageResults();
            string ConnectionString = String.Format("{0}", Configuration.GetConnectionString("DefaultConnection"));
            SqlConnection sqlConnection = new SqlConnection(ConnectionString);
            try
            {
                SqlCommand cmd = sqlConnection.CreateCommand();
                cmd.CommandType = CommandType.StoredProcedure;
                cmd.CommandText = "AddConfigSystem";

                cmd.Parameters.AddWithValue("@ConfigCode", (config.ConfigCode == null ? DBNull.Value : (object)config.ConfigCode));
                cmd.Parameters.AddWithValue("@ConfigName", config.ConfigName == null ? DBNull.Value : (object)config.ConfigName);
                cmd.Parameters.AddWithValue("@ConfigValue", config.ConfigName == null ? DBNull.Value : (object)config.ConfigValue);

                if (sqlConnection.State == ConnectionState.Closed)
                {
                    sqlConnection.Open();
                }
                _result = BaseService.GetMessageResults(cmd.ExecuteReader());
                BaseService.AddUserActive(currentUserID, IpAddress, _result, ConnectionString);
                return _result;
            }
            catch (Exception ex)
            {

                _Nlog.Error("\r\nMethod Name={0}\r\nMessage={1}\r\nStackTrace={2}\r\nInnerException={3}\r\nInnerException.Message={4}",
                    MethodBase.GetCurrentMethod()?.Name,
